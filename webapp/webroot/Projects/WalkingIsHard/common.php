<h3 id="WalkingIsHard">Walking Is Hard</h3>
<p>Cross-platform Java 8</p>
<p>Creatures that evolve to traverse increasingly difficult terrain.</p>
<p>The creature's DNA describes its joints and muscles that connect them, as well as all their attributes. The muscles are in physics terms a spring and a dampener with a rest state that varies from 1 to 0.5 times their original length from a sinus function over time with an aribitrary phase and frequency.</p>
<p>After the first lanuch "config.properties" will be created where you can tweak settings.</p>

